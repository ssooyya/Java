package operator;

public class Oper5 {
	public static void main(String[] args) {
		//증감연산자
		int data = 10;
		
		System.out.println(data);
		System.out.println(++data);
		System.out.println(data++);
		System.out.println(data);
		System.out.println(--data);
		
		int number = 100;
		number += 500;
		System.out.println(number);
	}
}